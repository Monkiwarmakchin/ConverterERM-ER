    //Funcion que recibe las entidades fuertes y las agrega a las entidades de esta ventana
    public void Relaciones(ArrayList<Entidad> Fuertes){
        TamAnt = Fuertes.size(); //Se obtiene el tama√±o del arreglo de las entidades fuertes
        Debiles = (ArrayList) Fuertes.clone(); // Se clonan las entidades fuertes en las entidades de esta ventana (Debiles)
        llenarcbx(); //Se encarga de llenar el Combobox de Entidades
    }
    
    //Se encarga de llenar el Combobox de Entidades Fuertes
    private void llenarcbx(){
        for(int i=0; i<Debiles.size(); i++)
        {
            cbxEntidad.addItem(Debiles.get(i).Nombre); 
        }
        //Para llenar el combobox (Utilizalo con un for para llenarlo ya que empiza vacio)
    }
    
    //Funcion que crea y extrae los datos de los elementos de la ventana
    private void Crear(){
        
        int i=0;
        ArrayList<Atributo> DebAtribs = new ArrayList<Atributo>();
        
        cbxEntidad.getSelectedItem(); //Regresa la cadena del elemento seleccionado del combobox
        txtNombre.getText(); //Regresa la cadena del campo de Nombre
        txtPK.getText(); //Regresa la cadena del campo de Llave Primaria
        
        Atributo NewPK = new Atributo(txtPK.getText(), true, false);
        DebAtribs.add(NewPK);
        
        for(i=0; i<listA.getSize(); i++)
        {
            System.out.println(""+listA.getElementAt(i)); //Donde listA.getElementAt(i) es la cadena en el espacio 'i' de la lista
            Atributo NewAtrib = new Atributo(listA.getElementAt(i).toString(), false, false);
            DebAtribs.add(NewAtrib);
        }
        
        for(i=0; i<Dependientes.size(); i++)
        {
            Atributo NewPF = new Atributo(Debiles.get(Debiles.indexOf(Dependientes.get(i).Padre)).Atribs.get(0).Nombre, true, true);
            DebAtribs.add(NewPF);
            for(int j=0; j<Dependientes.get(i).Atribs.size(); j++)
            {
                DebAtribs.add(Dependientes.get(i).Atribs.get(j));
            }
        }
        
        Entidad NewEnt = new Entidad(txtNombre.getText(), 'D', 'N', null, false, DebAtribs);
        Debiles.add(NewEnt);
    
    }
    
    //Funcion que elimna la entidad seleccionada en la lista de entidades
    private void Eliminar(int Ind){ //Vas a tener la cadena de la entidad que se quiere eliminar
       Debiles.remove(Debiles.get(Ind+TamAnt));
    }
    
    //Funcion que evalua ciertas condiciones para habilitar o inhabilitar el boton de Crear
    private void permisoCrear(){
    
        //Revisa que los campos de texto de Nombre y Llave primaria no esten vacios ademas de que la lista de atributos no est√© vacia
        if(txtNombre.getText().length()==0 || txtPK.getText().length()==0|| listC.isEmpty())
            btnCrear.setEnabled(false); //Habilita el boton de Crear
        else
            btnCrear.setEnabled(true); //Inhabilita el boton de Crear
    
    }
    
