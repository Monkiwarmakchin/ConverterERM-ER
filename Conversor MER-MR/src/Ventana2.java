    private void btnAgregarEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarEActionPerformed
        
        ArrayList<Atributo> DepAtribs = new ArrayList<Atributo>(); //Atributos ingresados
        
        for(int i=0; i<listD.getSize(); i++){
            System.out.println(""+listD.getElementAt(i)); //Donde listA.getElementAt(i) es la cadena en el espacio 'i' de la lista
            Atributo NewAtrib = new Atributo(listD.getElementAt(i).toString(), false, false); //Creamos un atributo por cada elemento puesto en la lista de "Otros atributos"...
            DepAtribs.add(NewAtrib);                                                          //y los agregamos en los atributos ingresados
        }
        
        Entidad NewDep = new Entidad(cbxEntidad.getName(), 'N', 'N', Debiles.get(cbxEntidad.getSelectedIndex()), true, DepAtribs);
        Dependientes.add(NewDep);
        
        listC.addElement(cbxEntidad.getSelectedItem());
        cbxEntidad.setSelectedIndex(0);
        listD.clear();
        permisoCrear();
        
    }//GEN-LAST:event_btnAgregarEActionPerformed

